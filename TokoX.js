function countProfit(shoppers) {
  if (shoppers.length < 1) {
    return shoppers
  }
  
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];
  var temp = []
  
  
  for (i = 0; i < listBarang.length; i++) {
    var productShopping = listBarang[i][0]
    var priceShopping = listBarang[i][1]
    var stockShopping = listBarang[i][2]

    var obj = {
        product: productShopping,
        shoppers: [],
        leftOver: stockShopping,
        totalProfit: 0
      }
     

    for (j = 0; j < shoppers.length; j++) {
      var shoppersName = shoppers[j].name
      var productMatch = shoppers[j].product
      var totalCart = shoppers[j].amount
    
      if(productMatch == productShopping && stockShopping >= totalCart && obj.leftOver >= totalCart ) {
        obj.shoppers.push(shoppersName)
        obj.leftOver -= totalCart
        obj.totalProfit = priceShopping * (stockShopping - obj.leftOver)
      }
    }
   temp.push(obj) 
  }
return (temp)
}

